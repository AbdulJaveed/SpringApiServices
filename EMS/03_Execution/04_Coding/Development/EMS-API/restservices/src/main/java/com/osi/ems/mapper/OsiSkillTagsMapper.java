package com.osi.ems.mapper;

import java.util.List;

import com.osi.ems.domain.OsiSkillTags;
import com.osi.ems.service.dto.OsiSkillTagsDto;

public interface OsiSkillTagsMapper {

	/**
	 * Method is for getting the osiSkilltagsDtoFrom osiSkilltags.
	 * @param osiSkillTags
	 * @return Returns the {@link OsiSkillTagsDto} object.
	 */
	public OsiSkillTagsDto OsiSkillTagToOsiSkillTagsDto(OsiSkillTags osiSkillTags);

	/**
	 * Method is for getting the osiSkillTags object from {@link OsiSkillTagsDto} object.
	 * @param osiSkillTagsDto 
	 * @return Returns the osiSkillTags object.
	 */
	public OsiSkillTags osiSkillTagsDtoToOsiSkillTags(OsiSkillTagsDto osiSkillTagsDto);

	/**
	 * The method is for getting the osiSkilltags list from osiskilltags dto list.
	 * @param osiSkillTagsDtoList
	 * @return Returns the {@link List} of {@link OsiSkillTags} object.
	 */
	public List<OsiSkillTags> osiSkillTagsDtoListToOsiSkillTagsList(List<OsiSkillTagsDto> osiSkillTagsDtoList);

	/**
	 * The method is for getting the {@link OsiSkillTagsDto} from osiSkilltags list.
	 * @param osiSkillTagsList
	 * @return Returns the {@link List} of {@link OsiSkillTagsDto} object.
	 */
	public List<OsiSkillTagsDto> osiSkillTagsListToOsiSkillTagsDtoList(List<OsiSkillTags> osiSkillTagsList);

}
