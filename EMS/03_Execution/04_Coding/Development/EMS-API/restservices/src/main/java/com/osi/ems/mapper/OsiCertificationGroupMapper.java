package com.osi.ems.mapper;

import java.util.List;

import com.osi.ems.domain.OsiCertificationGroups;
import com.osi.ems.service.dto.OsiCertificationGroupsDTO;

public interface OsiCertificationGroupMapper {

	/**
	 * Method for mapping the osiCertificationgroups from osiCertificationgroups dto.
	 * 
	 * @param osiCertificationGroupsDTO
	 * @return Returns the {@link OsiCertificationGroups}.
	 */
	public OsiCertificationGroups osiCertificationGroupsDtoToOsiCertificationGroups(
			OsiCertificationGroupsDTO osiCertificationGroupsDTO);

	/**
	 * Methdo for mapping the {@link OsiCertificationGroupsDTO} from
	 * {@link OsiCertificationGroups}
	 * 
	 * @param osiCertificationGroups
	 * @return Returns the {@link OsiCertificationGroupsDTO} object.
	 */
	public OsiCertificationGroupsDTO osiCertificationGroupsToOsiCertificationGroupsDTO(
			OsiCertificationGroups osiCertificationGroups);

	/**
	 * Method for mapping the list of {@link OsiCertificationGroups} from list of
	 * {@link OsiCertificationGroupsDTO}
	 * 
	 * @param osiCertificationGroupsDtoList
	 * @return Returns the list of {@link OsiCertificationGroups}.
	 */
	public List<OsiCertificationGroups> osiCertificationGroupsDtoListToOsiCertificationGroupsList(
			List<OsiCertificationGroupsDTO> osiCertificationGroupsDtoList);

	/**
	 * Method for Mapping the {@link OsiCertificationGroupsDTO} list from
	 * {@link OsiCertificationGroups}
	 * 
	 * @param osiCertificationGroupsList
	 * @return Returns the List of {@link OsiCertificationGroupsDTO} object.
	 */
	public List<OsiCertificationGroupsDTO> osiCertificationGroupsListToOsiCertificationGroupsDTO(
			List<OsiCertificationGroups> osiCertificationGroupsList);

}
