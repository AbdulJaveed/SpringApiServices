package com.osi.urm.domain;

public class ContactDetails {
	
	
	private String preference;
	private String name;
	private String mobileNumber;
	private String relation;
	private String altrMobileNumber;
	private String employeeId;
	private String createdBy;
	private String updatedBy;
	
	
	
	
	public String getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	public String getPreference() {
		return preference;
	}
	public void setPreference(String preference) {
		this.preference = preference;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getRelation() {
		return relation;
	}
	public void setRelation(String relation) {
		this.relation = relation;
	}
	public String getAltrMobileNumber() {
		return altrMobileNumber;
	}
	public void setAltrMobileNumber(String altrMobileNumber) {
		this.altrMobileNumber = altrMobileNumber;
	}
	public String getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}
	
	
	
	
	
	
	

}
