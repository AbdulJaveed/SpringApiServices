package com.osi.rm.vo;

public class OSIRMSchedulerFrequencies {
	private int scheduleFrequencyId;
	private String scheduleFrequencyName;
	
	public OSIRMSchedulerFrequencies(){}

	public int getScheduleFrequencyId() {
		return scheduleFrequencyId;
	}

	public void setScheduleFrequencyId(int scheduleFrequencyId) {
		this.scheduleFrequencyId = scheduleFrequencyId;
	}

	public String getScheduleFrequencyName() {
		return scheduleFrequencyName;
	}

	public void setScheduleFrequencyName(String scheduleFrequencyName) {
		this.scheduleFrequencyName = scheduleFrequencyName;
	}
}
